package com.lucas.xaudio.utils;

import android.app.ActivityManager;
import android.content.Context;
import android.util.DisplayMetrics;
import android.view.WindowManager;

import java.io.File;
import java.nio.ByteBuffer;
import java.nio.ByteOrder;
import java.util.List;

public class XAudioUtils {

  /**
   * 毫秒转分秒
   */
  public static String formatTime(long time) {
    String min = (time / (1000 * 60)) + "";
    String second = (time % (1000 * 60) / 1000) + "";
    if (min.length() < 2) {
      min = 0 + min;
    }
    if (second.length() < 2) {
      second = 0 + second;
    }
    return min + ":" + second;
  }


  /**
   * 判断Service是否正在运行
   *
   * @param context     上下文
   * @param serviceName Service 类全名
   * @return true 表示正在运行，false 表示没有运行
   */
  public static boolean isServiceRunning(Context context, String serviceName) {
    ActivityManager manager = (ActivityManager) context.getSystemService(Context.ACTIVITY_SERVICE);
    List<ActivityManager.RunningServiceInfo> serviceInfoList = manager.getRunningServices(200);
    if (serviceInfoList.size() <= 0) {
      return false;
    }
    for (ActivityManager.RunningServiceInfo info : serviceInfoList) {
      if (info.service.getClassName().equals(serviceName)) {
        return true;
      }
    }
    return false;
  }

  /**
   * 删除文件
   *
   * @param filePath     要删除的文件
   */
  public static void deleteFile(String filePath) {
    File file = new File(filePath);
    if (file.exists()) {
      if (file.isFile()) {
        file.delete();
      } else {
        String[] filePaths = file.list();
        for (String path : filePaths) {
          deleteFile(filePath + File.separator + path);
        }
        file.delete();
      }
    }
  }

  /**
   * byte[]转short[]
   *
   * @param bytes
   */
  public static short[] bytesToShort(byte[] bytes) {
    if (bytes == null) {
      return null;
    }
    short[] shorts = new short[bytes.length / 2];
    ByteBuffer.wrap(bytes).order(ByteOrder.LITTLE_ENDIAN).asShortBuffer().get(shorts);
    return shorts;
  }

  /**
   * short[]转byte[]
   *
   * @param shorts
   */
  public static byte[] shortToBytes(short[] shorts) {
    if (shorts == null) {
      return null;
    }
    byte[] bytes = new byte[shorts.length * 2];
    ByteBuffer.wrap(bytes).order(ByteOrder.LITTLE_ENDIAN).asShortBuffer().put(shorts);

    return bytes;
  }

}


